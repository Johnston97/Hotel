public class Employee {
    private String name;
    private int age;
    private String role;
    private double wage;
    private int hours;

    public Employee(String name, int age, String role, double wage, int hours){
        this.name = name;
        this.age = age;
        this.role = role;
        this.wage = wage;
        this.hours = hours;

    }
}
